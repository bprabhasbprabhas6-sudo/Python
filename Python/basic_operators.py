first_number = int(input("enter first number:"))
second_number = int(input("enter second number:"))
sum = first_number + second_number
print("Addition of two numbers is:", sum)
difference = first_number - second_number
print("Subtraction of two numbers is:", difference)
product = first_number * second_number
print("Multiplication of two numbers is:", product)
quotient = first_number / second_number
print("Division of two numbers is:", quotient)
modulus = first_number % second_number
print("Modulus of two numbers is:", modulus)
exponentiation = first_number ** second_number
print("Exponentiation of two numbers is:", exponentiation)
floor_division = first_number // second_number
print("Floor Division of two numbers is:", floor_division)
