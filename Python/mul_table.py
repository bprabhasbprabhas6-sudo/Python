number = int(input("Enter a number: "))
for i in range(1, 11):
    product = number * i
    print(f"{number} x {i} = {product}")
# This program prints the multiplication table of a given number from 1 to 10.
# Example usage:
# Enter a number: 5
# 5 x 1 = 5
# 5 x 2 = 10
# 5 x 3 = 15
# 5 x 4 = 20
# 5 x 5 = 25
# 5 x 6 = 30
# 5 x 7 = 35
# 5 x 8 = 40
# 5 x 9 = 45
# 5 x 10 = 50
# Note: You can change the range to print more or fewer multiples as needed.