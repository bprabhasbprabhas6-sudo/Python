number = int(input("Enter a number: "))
if number % 2 == 0:
    print(number, "is an even number")
else:
    print(number, "is an odd number")
# This program checks if a given number is even or odd.
# Example usage:
# Enter a number: 10
# 10 is an even number
# Enter a number: 7
# 7 is an odd number
